int yaffs_hweight8(u8 x)
{
	int ret_val;
	ret_val = yaffs_count_bits_table[x];
	return ret_val;
}

int yaffs_hweight32(u32 x)
{
	return yaffs_hweight8(x & 0xff) +
		yaffs_hweight8((x >> 8) & 0xff) +
		yaffs_hweight8((x >> 16) & 0xff) +
		yaffs_hweight8((x >> 24) & 0xff);
}

