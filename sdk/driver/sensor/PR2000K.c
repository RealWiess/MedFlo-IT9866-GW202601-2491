uint8_t
PR2000IsSignalStable (
    uint16_t Mode)
{
    static uint32_t timeout    = 0x0;
    uint8_t reg0        = 0x0;
    uint8_t reg1        = 0x0;
    uint8_t i           = 0x0;
    uint8_t std         = 0x0;
    uint8_t ref         = 0x0;
    uint8_t res         = 0x0;
    bool    isStable    = false;

    PR2000_WriteI2c_Byte(0xFF,0x0);
    reg0 = PR2000_ReadI2c_Byte(0x0);
    (void)printf("PR2000 reg(0x00) = %x\n", reg0);

    if ((reg0 & 0x8) == 0x8)
    {
        if(CurState == PR2000_UNDECT)
        {
            timeout = 0;           
            reg0 = PR2000_ReadI2c_Byte(0x0);
            for(i = 0; i < PR2000_MAX_ID; i++)
            {
                std = (reg0 & REG0_DET_IMFT_STD_MASK) >> REG0_DET_IMFT_STD_SHIF;
                ref = (reg0 & REG0_DET_IMFT_REF_MASK) >> REG0_DET_IMFT_REF_SHIF;
                res = (reg0 & REG0_DET_IMFT_RES_MASK) >> REG0_DET_IMFT_RES_SHIF;
                (void)printf("std = %x  res= %x , ref = %x\n", std, res, ref);

                if(std == p2k_info_table[i].imft_std)
                {
                    if((ref == p2k_info_table[i].imft_ref) && (res == p2k_info_table[i].imft_res))
                    {
                        CurResolution = i;
                        pr2000_set_table();
                        PR2000_WriteI2c_Byte(0xFF,0x0);
                        PR2000_WriteI2c_Byte(0x14, ((0x20) | (CurVIN & 0xF))); 
                        CurState = PR2000_DECT;
                    }
                }
            }
        }
        else if(CurState == PR2000_DECT)
        {
            reg1 = PR2000_ReadI2c_Byte(0x1);
            (void)printf("PR2000 reg(0x01) = %x\n", reg1);

            if(((reg1 & 0x7F) == 0x7F))
            {
                CurState = PR2000_LOCK;
                isStable = true;
                (void)printf("PR2000 stable, Lock!\n");
            }
            else
            {
                timeout++;
                if(timeout > 50)
                {
                    //(void)printf("PR2000 unstable time out!\n");
                    CurState = PR2000_UNDECT;
                }
            }
        }
        else if(CurState == PR2000_LOCK)
        {
            isStable = true;
        }
    }
    else
    {
        PR2000_WriteI2c_Byte(0xFF,0x0);
        PR2000_WriteI2c_Byte(0x11,0x0);

        CurState = PR2000_UNDECT;
        isStable = false;
    }
    
    /* stable return true else return false               */
    return isStable;
}

