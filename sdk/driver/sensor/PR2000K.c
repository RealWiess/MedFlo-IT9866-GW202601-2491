void
PR2000_initial_table (
    REGPAIR         * table,
    unsigned int    count)
{
    int i = 0;
    //printf("PR  %s ENTER  %d\n",__func__,__LINE__);
    for (i = 0; i < count; i++)
    {
        // (void)printf("(w (addr = %x, value = %x)\n",table[i].addr,table[i].value);
        PR2000_WriteI2c_Byte((table[i].addr & 0xff), table[i].value);
    }
}

