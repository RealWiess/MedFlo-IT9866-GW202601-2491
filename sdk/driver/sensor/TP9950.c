void
TP9950_initial_table (
    REGPAIR         * table,
    unsigned int    count)
{
    int i = 0;

    for (i = 0; i < count; i++)
    {
        // (void)printf("(w (addr = %x, value = %x)\n",table[i].addr,table[i].value);
        TP9950_WriteI2c_Byte((table[i].addr & 0xff), table[i].value);
    }
}

