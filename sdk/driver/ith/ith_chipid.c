#define CHIP_INFO_SIZE (16 * 8)

bool ithGetChipInfo(void)
{
    int i;
    uint8_t* src                 = (uint8_t*)itpVmemAlloc(CHIP_INFO_SIZE);
    uint8_t* dst                 = (uint8_t*)itpVmemAlloc(CHIP_INFO_SIZE);

    static uint8_t RAND_Data[CHIP_INFO_SIZE] = {
        0xc6U, 0x7eU, 0x81U, 0x6bU, 0x4bU, 0xfbU, 0xe2U, 0xfbU, 0x54U, 0xf6U,
        0xbdU, 0xdfU, 0x7cU, 0x1cU, 0xe1U, 0x87U, 0x01U, 0xbfU, 0x31U, 0xdeU,
        0x56U, 0x72U, 0x0fU, 0x47U, 0x67U, 0x66U, 0x87U, 0x59U, 0xaaU, 0x88U,
        0x3cU, 0x59U, 0xeaU, 0x56U, 0x13U, 0x7bU, 0xd2U, 0x85U, 0xa1U, 0xd8U,
        0x3cU, 0x54U, 0x55U, 0x2fU, 0x37U, 0xaeU, 0x65U, 0x5bU, 0xdaU, 0x02U,
        0x79U, 0x98U, 0xccU, 0xe3U, 0x1aU, 0x76U, 0x8eU, 0x5fU, 0xd9U, 0x99U,
        0x8fU, 0x1fU, 0x3fU, 0x36U, 0xeeU, 0x43U, 0x78U, 0x4dU, 0x0dU, 0xfaU,
        0xbeU, 0xa6U, 0xdaU, 0xe4U, 0x86U, 0x8eU, 0xdcU, 0x29U, 0x6dU, 0x4eU,
        0xffU, 0x56U, 0xe1U, 0x70U, 0x20U, 0xfbU, 0x8fU, 0xb1U, 0x58U, 0x05U,
        0x90U, 0xc5U, 0x09U, 0xdcU, 0x53U, 0xcdU, 0xaaU, 0x3bU, 0x48U, 0x99U,
        0x52U, 0xd3U, 0x52U, 0x9dU, 0x06U, 0x9fU, 0xeaU, 0xb5U, 0xc2U, 0x06U,
        0x13U, 0x98U, 0x49U, 0xb2U, 0x01U, 0x1eU, 0xacU, 0x32U, 0x88U, 0x31U,
        0x9cU, 0x52U, 0x46U, 0x95U, 0x71U, 0x36U, 0x8fU, 0x57U};

    uint8_t Data[CHIP_INFO_SIZE] = {
        0xffU, 0xffU, 0xc6U, 0x7eU, 0x22U, 0x6cU, 0x68U, 0xfbU, 0xafU, 0xf5U,
        0xdcU, 0xaeU, 0xe8U, 0x66U, 0xc4U, 0xbeU, 0x74U, 0x93U, 0xedU, 0x46U,
        0x07U, 0x5bU, 0xadU, 0x78U, 0x0fU, 0x55U, 0xffU, 0xffU, 0xffU, 0xffU,
        0xffU, 0xffU, 0xffU, 0xffU, 0xc7U, 0x6eU, 0x05U, 0x6cU, 0xa9U, 0xe3U,
        0x11U, 0xd5U, 0xfbU, 0x96U, 0x29U, 0x66U, 0xa9U, 0x95U, 0x53U, 0xa3U,
        0xd0U, 0x4eU, 0x6aU, 0x63U, 0x4dU, 0x6aU, 0xb1U, 0x4cU, 0xffU, 0xffU,
        0xffU, 0xffU, 0xffU, 0xffU, 0xffU, 0xffU, 0xc8U, 0x5eU, 0xecU, 0x6bU,
        0x2cU, 0xb4U, 0xd2U, 0x9bU, 0x17U, 0x5fU, 0xebU, 0x6cU, 0x94U, 0x3bU,
        0xd1U, 0xbaU, 0x56U, 0x66U, 0xf1U, 0x73U, 0xaeU, 0x35U, 0xd4U, 0x44U,
        0xffU, 0xffU, 0xffU, 0xffU, 0xffU, 0xffU, 0xffU, 0xffU, 0x2aU, 0x4eU,
        0x32U, 0x64U, 0x2eU, 0xa4U, 0x95U, 0x8bU, 0xd2U, 0x4eU, 0x6dU, 0x6cU,
        0x5aU, 0x1bU, 0x4dU, 0xc2U, 0x37U, 0x7dU, 0xb3U, 0x74U, 0xcdU, 0x46U,
        0xf4U, 0x4cU, 0xffU, 0xffU, 0xffU, 0xffU, 0xffU, 0xffU};

    (void)memcpy((void*)src, RAND_Data, CHIP_INFO_SIZE);
    (void)memset((void*)dst, 0xFF, CHIP_INFO_SIZE);

    ithFlushDCacheRange(src, CHIP_INFO_SIZE);
    ithFlushDCacheRange(dst, CHIP_INFO_SIZE);
    ithFlushMemBuffer();

    ithWriteRegMaskA(ITH_HOST_BASE + ITH_CQ_CLK_REG, 0x02af8000U, 0x02af8000U);

    {
        ithWriteRegA(0xb0700000U, (uint32_t)src);
        ithWriteRegA(0xb0700004U, 0x00000000U);
        ithWriteRegA(0xb0700008U, 0x00100004U);
        ithWriteRegA(0xb070000cU, 0x00000020U);
        ithWriteRegA(0xb0700010U, 0x00000000U);
        ithWriteRegA(0xb0700014U, 0x00000000U);
        ithWriteRegA(0xb0700018U, 0x00000000U);
        ithWriteRegA(0xb070001cU, 0x00000000U);
        ithWriteRegA(0xb0700020U, (uint32_t)dst);
        ithWriteRegA(0xb0700024U, 0x00001fffU);
        ithWriteRegA(0xb0700028U, 0x000f000cU);
        ithWriteRegA(0xb070002cU, 0x00000020U);
        ithWriteRegA(0xb0700030U, 0x0000000fU);
        ithWriteRegA(0xb0700034U, 0x00000003U);
        ithWriteRegA(0xb0700038U, 0x00000000U);
        ithWriteRegA(0xb0700040U, 0x000bc237U);
        ithWriteRegA(0xb0700044U, 0xffffef37U);
        ithWriteRegA(0xb0700048U, 0xffecf9daU);
        ithWriteRegA(0xb070004cU, 0x00000000U);
        ithWriteRegA(0xb0700050U, 0x0003de0fU);
        ithWriteRegA(0xb0700054U, 0xfffc21f1U);
        ithWriteRegA(0xb0700058U, 0xfffffdb1U);
        ithWriteRegA(0xb070005cU, 0x00000692U);
        ithWriteRegA(0xb0700060U, 0x0007fd2dU);
        ithWriteRegA(0xb0700064U, 0x00000000U);
        ithWriteRegA(0xb0700068U, 0x00000000U);
        ithWriteRegA(0xb0700074U, 0x000000ccU);
        ithWriteRegA(0xb070007cU, 0x000c2000U);
        ithWriteRegA(0xb0700080U, 0x000046c0U);
        ithWriteRegA(0xb0700084U, 0x00000c0cU);
        ithWriteRegA(0xb0700088U, 0x00000000U);
        ithWriteRegA(0xb070008cU, 0x00000001U);

        // wait 2d idle
        {
            uint32_t timeout = 5000U;
#ifdef GFX_USE_CMDQ
            ithCmdQWaitEmpty(ITH_CMDQ0_OFFSET);
#endif
            // Wait engine idle
            while (((ithReadRegA(0xB07000C4U) & (1U << 0U)) != 0U) && (timeout != 0U))
            {
                (void)usleep(200);
                timeout--;
            }
        }

        ithInvalidateDCacheRange(dst, CHIP_INFO_SIZE);

        for (i = 0; i < CHIP_INFO_SIZE; i++)
        {
            if (Data[i] != dst[i])
            {
                // i = 24 fail, Data[24]:0xf , dst[24]:0x3c
                (void)printf("Data[%d]:0x%x , dst[%d]:0x%x\n", i, Data[i], i, dst[i]);
                break;
            }
        }

        itpVmemFree((uint32_t)dst);
        itpVmemFree((uint32_t)src);
        ithWriteRegMaskA(ITH_HOST_BASE + ITH_CQ_CLK_REG, 0x00000000U, 0x02af8000U);

#if 0
        (void)printf("i:%d\n", i);
#endif
        if (i < CHIP_INFO_SIZE)
        {
            return false;
        }
        else
        {
            (void)printf("CHIP INFO CHECK END\n");
            return true;
        }
    }
}

